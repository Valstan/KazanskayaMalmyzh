// /years, и навигация «предыдущий / следующий год» на /years/<год>.
export const YEARS: YearInfo[] = [...YEARS_RAW].sort((a, b) => b.year - a.year)

/** Годы, у которых есть отдельная страница (есть абзацы). */
export function yearsWithPage(): number[] {
  return YEARS.filter((y) => y.paragraphs.length > 0).map((y) => y.year)
}

export function getYearInfo(year: number): YearInfo | undefined {
  return YEARS.find((y) => y.year === year)
}
